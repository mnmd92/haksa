package haksa.member;

public class MemberDTO {
	private String hakbun;
	private String password;
	private String daehak;
	private String hakgwa;
	private String jeongong;
	private String name;
	private String haknyeon;
	private String status;
	private String gijunhakjeom;
	
	public String getHakbun() {
		return hakbun;
	}
	public void setHakbun(String hakbun) {
		this.hakbun = hakbun;
	}
	public String getPassword() {
		return password;
	}
	public void setPassword(String password) {
		this.password = password;
	}
	public String getDaehak() {
		return daehak;
	}
	public void setDaehak(String daehak) {
		this.daehak = daehak;
	}
	public String getHakgwa() {
		return hakgwa;
	}
	public void setHakgwa(String hakgwa) {
		this.hakgwa = hakgwa;
	}
	public String getJeongong() {
		return jeongong;
	}
	public void setJeongong(String jeongong) {
		this.jeongong = jeongong;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getHaknyeon() {
		return haknyeon;
	}
	public void setHaknyeon(String haknyeon) {
		this.haknyeon = haknyeon;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	public String getGijunhakjeom() {
		return gijunhakjeom;
	}
	public void setGijunhakjeom(String gijunhakjeom) {
		this.gijunhakjeom = gijunhakjeom;
	}
	
}
